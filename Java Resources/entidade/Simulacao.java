package entidade;

public class Simulacao {
	
	private Long _id;	
	private String numeroPessoas;
	private String pratos;
	private String salada;
	private String carboidratos;
	private String proteina;
	private String suco;
	private String tempoTotal;
	private String tempoMedio;
	
	public Long get_id() {
		return _id;
	}
	public void set_id(Long _id) {
		this._id = _id;
	}
	public String getNumeroPessoas() {
		return numeroPessoas;
	}
	public void setNumeroPessoas(String numeroPessoas) {
		this.numeroPessoas = numeroPessoas;
	}
	public String getPratos() {
		return pratos;
	}
	public void setPratos(String pratos) {
		this.pratos = pratos;
	}
	public String getSalada() {
		return salada;
	}
	public void setSalada(String salada) {
		this.salada = salada;
	}
	public String getCarboidratos() {
		return carboidratos;
	}
	public void setCarboidratos(String carboidratos) {
		this.carboidratos = carboidratos;
	}
	public String getProteina() {
		return proteina;
	}
	public void setProteina(String proteina) {
		this.proteina = proteina;
	}
	public String getSuco() {
		return suco;
	}
	public void setSuco(String suco) {
		this.suco = suco;
	}
	public String getTempoTotal() {
		return tempoTotal;
	}
	public void setTempoTotal(String tempoTotal) {
		this.tempoTotal = tempoTotal;
	}
	public String getTempoMedio() {
		return tempoMedio;
	}
	public void setTempoMedio(String tempoMedio) {
		this.tempoMedio = tempoMedio;
	}

}
