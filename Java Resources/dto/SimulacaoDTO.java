package dto;

public class SimulacaoDTO {
	
	private Long numeroPessoas;
	private Long pratos;
	private Long salada;
	private Long carboidratos;
	private Long proteina;
	private Long suco;
	private Double tempoMaximo;
	private Double tempoMinimo;
	
	public Long getNumeroPessoas() {
		return numeroPessoas;
	}
	public void setNumeroPessoas(Long numeroPessoas) {
		this.numeroPessoas = numeroPessoas;
	}
	public Long getPratos() {
		return pratos;
	}
	public void setPratos(Long pratos) {
		this.pratos = pratos;
	}
	public Long getSalada() {
		return salada;
	}
	public void setSalada(Long salada) {
		this.salada = salada;
	}
	public Long getCarboidratos() {
		return carboidratos;
	}
	public void setCarboidratos(Long carboidratos) {
		this.carboidratos = carboidratos;
	}
	public Long getProteina() {
		return proteina;
	}
	public void setProteina(Long proteina) {
		this.proteina = proteina;
	}
	public Long getSuco() {
		return suco;
	}
	public void setSuco(Long suco) {
		this.suco = suco;
	}
	public Double getTempoMaximo() {
		return tempoMaximo;
	}
	public void setTempoMaximo(Double tempoMaximo) {
		this.tempoMaximo = tempoMaximo;
	}
	public Double getTempoMinimo() {
		return tempoMinimo;
	}
	public void setTempoMinimo(Double tempoMinimo) {
		this.tempoMinimo = tempoMinimo;
	}
	

}
